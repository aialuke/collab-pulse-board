type ToastProps = React.ComponentPropsWithoutRef<typeof Toast>

type ToastActionElement = React.ReactElement<typeof ToastAction>

// Define ToasterToast type
export type ToasterToast = {
  id: string
  title?: React.ReactNode
  description?: React.ReactNode
  action?: ToastActionElement
  variant?: VariantProps<typeof toastVariants>["variant"]
  open?: boolean
  onOpenChange?: (open: boolean) => void
}

// Define useToast hook properly as a React hook
export const useToast = () => {
  const [toasts, setToasts] = React.useState<ToasterToast[]>([])

  const toast = (props: Omit<ToasterToast, "id" | "open" | "onOpenChange">) => {
    const id = Math.random().toString(36).substring(2, 9)
    setToasts((prevToasts) => [...prevToasts, { id, ...props, open: true }])
    return {
      id,
      dismiss: () => setToasts((prevToasts) => prevToasts.filter((toast) => toast.id !== id)),
      update: (props: ToasterToast) =>
        setToasts((prevToasts) =>
          prevToasts.map((toast) => (toast.id === id ? { ...toast, ...props } : toast))
        ),
    }
  }

  const dismiss = (toastId?: string) => {
    setToasts((prevToasts) =>
      prevToasts.map((toast) => {
        if (toastId === undefined || toast.id === toastId) {
          return {
            ...toast,
            open: false,
          }
        }
        return toast
      })
    )
  }

  React.useEffect(() => {
    const timeoutIds: ReturnType<typeof setTimeout>[] = []

    toasts.forEach((toast) => {
      if (toast.open === false) {
        const timeoutId = setTimeout(() => {
          setToasts((prevToasts) => prevToasts.filter((t) => t.id !== toast.id))
        }, 300)
        timeoutIds.push(timeoutId)
      }
    })

    return () => {
      timeoutIds.forEach((id) => clearTimeout(id))
    }
  }, [toasts])

  return {
    toasts,
    toast,
    dismiss,
  }
}

export {
  type ToastProps,
  type ToastActionElement,
  ToastProvider,
  ToastViewport,
  Toast,
  ToastTitle,
  ToastDescription,
  ToastClose,
  ToastAction,
}
